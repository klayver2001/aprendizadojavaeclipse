package op_logicas_condicionais_relacionais;

public class DivisaoRestoMod {

	public static void main(String[] args) {
		
		/* Módulo: Resto da divisão % */
		
		double carro = 20;
		double pessoa = 3;
		
		double resto = carro % pessoa; // Abreviação MOD
		
		System.out.println("Sobram exatamente : " + resto + " carros.");

	}

}
