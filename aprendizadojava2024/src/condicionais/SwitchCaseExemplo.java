public class SwitchCaseExemplo {

	public static void main(String[] args) {
		
				/* SWITCH CASE : OPERAÇÕES EXATAS */
				int dia = 2	;
				switch (dia) {
				case 1:
					System.out.println("Domingo");
					break;
				case 2:
					System.out.println("Segunda");
					break;	

				default:System.out.println("" );
					break;
				}
	}
}
